package 基础.运算符;

public class Relationship {
    public static void main(String[] args) {
        /*
          1. >  大于
          2. <  小于
          3. == 等于
          4. <= 小于等于
          5. >= 大于等于
          6. != 不等于
         */

        // >  (正确返回true,错误返回false)(其他同)
        int a = 10;
        int b = 11;
        System.out.println(a>b);
        System.out.println(a<b);
    }
}
